    private void facultyTableMouseClicked(java.awt.event.MouseEvent evt) {                                          
        int row = facultyTable.getSelectedRow();

        txtId.setText(facultyTable.getValueAt(row, 0).toString());
        txtName.setText(facultyTable.getValueAt(row, 1).toString());
        txtDept.setSelectedItem(facultyTable.getValueAt(row, 2).toString());
        txtSubject.setSelectedItem(facultyTable.getValueAt(row, 3).toString());
        txtEmail.setText(facultyTable.getValueAt(row, 4).toString());

    }                                         

