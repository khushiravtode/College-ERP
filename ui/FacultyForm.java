    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        int id = Integer.parseInt(txtId.getText());
        String name = txtName.getText();
        String dept = txtDept.getSelectedItem().toString();
        String subject = txtSubject.getSelectedItem().toString();
        String email = txtEmail.getText();

        Faculty f = new Faculty(id, name, dept, subject, email);
        facultyService.addFaculty(f);

        JOptionPane.showMessageDialog(this, "Faculty Added Successfully");

        clearFields();

    }                                        

