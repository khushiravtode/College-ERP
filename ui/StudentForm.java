    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // 1️⃣ Get input from text fields
        int id = Integer.parseInt(txtStudentId.getText());
        String name = txtName.getText();
       String branch = txtBranch.getSelectedItem().toString();
        String year = txtYear.getSelectedItem().toString();
        String email = txtEmail.getText();

        // 2️⃣ Create new Student object
        Student s = new Student(id, name, branch, year, email);

        // 3️⃣ Add student to service
        studentService.addStudent(s);

        // 6️⃣ Success message
        JOptionPane.showMessageDialog(this, "Student Added Successfully!");
        loadStudents();
        clearFields();

    }                                        
    private void refreshStudentTable() {
        ArrayList<Student> list = studentService.getAllStudents();
        String[] columns = {"ID", "Name", "Branch", "Year", "Email"};
        String[][] data = new String[list.size()][5];

        for (int i = 0; i < list.size(); i++) {
            Student s = list.get(i);
            data[i][0] = String.valueOf(s.getId());
            data[i][1] = s.getName();
            data[i][2] = s.getBranch();
            data[i][3] = s.getYear();
            data[i][4] = s.getEmail();
        }

        tblStudents.setModel(new javax.swing.table.DefaultTableModel(data, columns));
    }

