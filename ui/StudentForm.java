    public void loadStudents() {
        DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
        model.setRowCount(0);

        ArrayList<Student> list = studentService.getAllStudents();

        for (Student s : list) {
            model.addRow(new Object[]{
                s.getId(),
                s.getName(),
                s.getBranch(),
                s.getYear(),
                s.getEmail()
            });
        }
    }

